# Начальная и конечная точки
start = (0, 0)
goal = (24, 24)

# Параметры Q-learning
alpha = 0.1  # Скорость обучения
gamma = 0.9  # Коэффициент дисконтирования
epsilon = 0.1  # Вероятность случайного действия
episodes = 1000  # Количество эпизодов

# Инициализация Q-матрицы
q_table = np.zeros((n, m, 4))  # 4 действия (вверх, вниз, влево, вправо)

# Определение функций действий
def is_valid_move(state, action):
    x, y = state
    if action == 0:  # Вверх
        return x > 0 and bottom_walls[x-1][y] == 0
    elif action == 1:  # Вниз
        return x < n - 1 and bottom_walls[x][y] == 0
    elif action == 2:  # Влево
        return y > 0 and right_walls[x][y-1] == 0
    elif action == 3:  # Вправо
        return y < m - 1 and right_walls[x][y] == 0
    return False

def take_action(state, action):
    x, y = state
    if action == 0:
        return (x - 1, y)
    elif action == 1:
        return (x + 1, y)
    elif action == 2:
        return (x, y - 1)
    elif action == 3:
        return (x, y + 1)
    return state

# Награда за достижение цели
def get_reward(state):
    if state == goal:
        return 100
    return -1

# Q-learning
for episode in range(episodes):
    print(episode)
    state = start  # Начальная точка
    for step in range(100):  # Ограничиваем количество шагов в эпизоде
        # Выбор действия
        if random.uniform(0, 1) < epsilon:
            action = random.choice([0, 1, 2, 3])  # Случайное действие
        else:
            action = np.argmax(q_table[state[0], state[1]])  # Действие с максимальным Q-значением
        
        if is_valid_move(state, action):
            new_state = take_action(state, action)
            reward = get_reward(new_state)
            
            # Обновление Q-значений
            q_table[state[0], state[1], action] += alpha * (reward + gamma * np.max(q_table[new_state[0], new_state[1]]) - q_table[state[0], state[1], action])
            
            state = new_state
            if state == goal:
                break

# Вывод Q-матрицы
print("Q-матрица:")
print(q_table)

# Вывод матрицы решения
solution = np.zeros((n, m), dtype=int)
state = start
while state != goal:
    action = np.argmax(q_table[state[0], state[1]])
    solution[state[0], state[1]] = action
    state = take_action(state, action)

print("Матрица решения:")
print(solution)
